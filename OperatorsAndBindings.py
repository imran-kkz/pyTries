# Unary and Bitwise Operators

a = 0b010

print (a)

# ~ two's compliment
# it takes a number that we're going to call x, and returns the result of -x - 1

print (~a)

# Main ones to know:

#AND OR XOR NOT

b = 0b1001
c = 0b1100

print (bin(b | c)) # OR

print (bin(b & c)) # AND

print (bin(b ^ c)) # XOR (exclusive OR)

